#include<iostream>
using namespace std;
int main()
{
 cout<<"Size of a Integer variable:"<<sizeof(int)<<"\tSize of a Integer pointer:"<<sizeof(int*)<<endl;
 cout<<"Size of a Float variable:"<<sizeof(float)<<"\tSize of a Float pointer:"<<sizeof(float*)<<endl;
 cout<<"Size of a Character variable:"<<sizeof(char)<<"\tSize of a Character pointer:"<<sizeof(char*)<<endl;
 cout<<"Size of a Boolean variable:"<<sizeof(bool)<<"\tSize of a Boolean pointer:"<<sizeof(bool*)<<endl;
 return 0;
}
